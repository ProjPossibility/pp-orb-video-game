/**
 * Class Double Position take the position as type double
 * @author Thuy Truong
 * University of Southern California
 * Code for a Cause 2007
 */
public class DPosition {
	double x;
	double y;
	
	public DPosition(){
		x = 0.0;
		y = 0.0;
	}
	
	public DPosition(double x, double y){
		this.x = x;
		this.y = y;
	}
}
