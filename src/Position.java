/**
 * Position class
 * 2 parameter, x-position and y-position
 * @author Thuy Truong
 * University of Southern California
 * Code for a Cause 2007
 */

public class Position {
	//variable declaration
	public Integer x;
	public Integer y;
	
	//default constructor
	public Position() {
		x = 0;
		y = 0;
	}
	
	public Position(int xNew, int yNew){
		x = xNew;
		y = yNew;
	}
	
}
